# --- Задача 1 ---
# Дан массив из 10 целых чисел. Определить наименьшее число, которое делится на 5 без остатка.

numbers = [12, 25, 40, 7, 15, 33, 5, 60, 8, 20]
multiples_of_5 = [num for num in numbers if num % 5 == 0]

if multiples_of_5:  # проверяем, есть ли такие числа
    print("Наименьшее число, которое делится на 5:", min(multiples_of_5))
else:
    print("В массиве нет чисел, которые делятся на 5.")

# --- Задача 2 ---
# Найти наименьший элемент на дополнительной диагонали матрицы 4x4.

A = [
    [4,  7, 12, 9],
    [5, 11,  3, 8],
    [6, 14, 10, 2],
    [13,  1, 15, 16]
]

# Доп. диагональ — это элементы, где сумма индексов равна n-1 (для 4×4 -> 3)
n = 4
additional_diag = [A[i][n - 1 - i] for i in range(n)]

print("Дополнительная диагональ:", additional_diag)
print("Минимальный элемент на доп. диагонали:", min(additional_diag))
